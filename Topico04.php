                                <h2 class="Titulo" >Tópico 4 - O paradigma da Nova Gestão Pública</h2>
                                <br>

                                <p class="Texto">Durante as décadas de 1980 e de 1990, o mundo observou e vivenciou um profundo e amplo processo de modernização da administração pública e de reforma do Estado, baseado em um modelo que ficou conhecido como a New Public Management – NPM ( Nova Gestão Pública). </p>

                                <p class="Texto">Seu surgimento e sua consolidação decorrem basicamente de quatro fatores:</p>

                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a ineficácia, a ineficiência e a incapacidade de mudança e de adaptação da gestão burocrática, até então o padrão básico da administração pública;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a crise financeira-fiscal que se abateu praticamente sobre todos os países do mundo, a partir da década de 1970, com graves reflexos negativos sobre a capacidade dos Estados exercerem seus papéis tradicionais de promotor do bem-estar social e/ou do desenvolvimento econômico dos países;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a concentração e a predominância, nos principais centros de poder mundiais, de uma ideologia com fortes características neoliberais e privatistas;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> o fortalecimento da globalização, associado ao aumento do poder das multinacionais e às transformações tecnológicas, políticas e socioeconômicas ocorridas em todo o mundo, levando ao enfraquecimento do poder e controle dos governos e dos próprios Estados Nacionais.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">Na realidade, as críticas e reações às ineficiências e às ineficácias da administração burocrática, bem como às suas dificuldades de adaptação às mudanças externas e internas, surgiram já nos anos 50, a partir de estudos e análises teórico-conceituais de vários especialistas, como já mencionado.</p>

                                <p class="Texto">No lado mais prático, a insatisfação com a ineficiência e a incapacidade adaptativa da gestão burocrática levou a administração pública a desenvolver e implantar instrumentos e medidas para sua superação. Assim, foi sendo concebido, desenvolvido e aplicado, nos mais diferentes países, um amplo e diversificado acervo de novos conceitos e práticas de gestão pública, tais como:</p>

                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> modelos gerenciais;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> avaliação de desempenho;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> controle orçamentário de pessoal e de serviços públicos;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> programas de desburocratização e de redução de gastos;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> entre tantos outros.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubTitulo">4.1 - O Gerencialismo Puro</p>

                                <p class="Texto">A existência de uma elevada insatisfação com a gestão burocrática dominante, a ascensão de teorias extremamente críticas à burocracia estatal, a crença dominante na opinião pública, sobre a superioridade do modelo privado de gestão, aliadas circunstancialmente à vitória de lideranças neoliberais no Reino Unido e nos Estados Unidos, criaram as condições necessárias para a implantação e a disseminação do modelo gerencial na administração.</p>

                                <p class="Texto">É justamente nesse contexto que surge e se instala o modelo do gerencialismo puro, cuja premissa básica é a de que as soluções para as deficiências da administração pública estão centradas na redução do tamanho do Estado e na equiparação da função pública ao modelo gerencial privado, por meio da adoção de sistemas e técnicas vigentes nas empresas. Seus principais legados são:</p>

                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> as medidas de desburocratização, tanto interna quanto em relação aos cidadãos;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> os instrumentos para aumentar a economia e a eficiência do setor público; e</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a defesa de uma cultura gerencial mais empreendedora.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">No entanto, o gerencialismo puro adotou uma visão negativa da intervenção estatal e burocrática, deixando de captar a relevância da ação governamental no mundo contemporâneo, sem perceber a especificidade e a necessidade da burocracia como contraponto ao patrimonialismo e à garantia da estabilidade das sociedades contemporâneas. Além disso, não considerou que a gestão pública tinha uma forma de operação e uma ética diferente do setor privado, especialmente em relação aos desígnios democráticos e à necessidade de produzir serviços públicos para além da eficiência.</p>

                                <p class="Texto">Todavia, o grande legado do gerencialismo puro talvez seja sua ideologia (e não suas ações e instrumentos), que ficou impregnada nas demais versões de reforma administrativa e nos modelos de gestão pública, que ainda prevalecem fortemente em muitos contextos nos dias de hoje.</p>
                                <br>

                                <p class="SubTitulo">4.2- A Nova Gestão Pública: diversidade, amplitude e evolução</p>

                                <p class="Texto">Depois de várias tentativas fracassadas de modificar os parâmetros weberianos, que dominaram as administrações públicas por muito tempo – e ainda dominam, em muitos contextos –, apareceu, nos anos 80 e 90, de forma avassaladora e em numerosos países, um novo paradigma de reforma administrativa, sob a denominação ampla de Nova Gestão Pública (NGP).</p>

                                <p class="Texto">Na verdade, a Nova Gestão Pública, apesar de sua forte influência e ampla disseminação, em praticamente todo o mundo, não constituiu um movimento unificado, organizado ou convergente, baseado em modelo padronizado, de aceitação universal. Na verdade, a NGP foi sendo concebida e implantada de forma progressiva, dando margem a uma variedade de correntes, tendo em comum, basicamente, a certeza da superioridade dos modelos, técnicas e ferramentas do setor privado e a consequente garantia dos impactos positivos de sua aplicação na administração pública.</p>

                                <p class="Texto">No entanto, mesmo considerando a extrema diversidade dos modelos e de correntes de reforma administrativa, abrigados sob o termo Nova Gestão Pública, alguns especialistas tentaram sintetizar um conjunto de princípios e características comuns, que constituem o quadro básico da Nova Gestão Pública.</p>

                                <p class="SubSubTitulo">Características Gerais da Nova Gestão Pública</p>
                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> mudança no foco de gestão: de processo para resultados de interesse dos clientes (cidadãos);</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> ampliação do uso de medições e quantificações, em especial no que diz respeito à perfomance;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> ampliação do uso de medições e quantificações, em especial no que diz respeito à perfomance;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> implementação de estruturas mais enxutas e descentralizadas;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> uso de “divisões” como forma de quebrar grandes unidades burocráticas e aumento de parcerias com organizações do setor privado e da sociedade civil;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> ampla ênfase em relações contratuais, tanto nas relações com fornecedores externos quanto na própria provisão interna de serviços; e</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> maior atenção à gestão organizacional e às habilidades gerenciais dos servidores públicos, incluindo orientação para o consumidor e sistemas baseados na avaliação de performance.</p>
                                    </li>
                                </ul>
                                <p class="Texto"> POLLITT; VAN THIEL E HOMBUG(2007 apud FARIA 2009)</p>
                                <br>

                                <p class="Texto">Além da diversidade de modelos e correntes de NGP, constataram-se igualmente variações nas suas implantações ao redor do mundo. </p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>“A Nova Gestão Pública foi o paradigma de reforma administrativa que prevaleceu até meados dos anos 1990, acompanhando a hegemonia da agenda neoliberal. Naturalmente, não se aplicou em todos os países por igual: teve grande influência nos países anglo-americanos – ainda que com diferenças entre eles – menos nos países nórdicos e escassa nos países de matriz latino-americana e germânica. Na Espanha, sua influência prática foi escassa, ainda que desigual.”</em>
                                    </p>
                                    <p class="Texto"><em>– PRATS I CATALÀ (2005, p.114.)</em></p>
                                </div>

                                <p class="Texto">Outra característica, que tem sido bastante observada nas trajetórias de implantação de reformas administrativas, segundo os preceitos da NGP, é a progressiva incorporação da ética e de valores públicos, sem, contudo, significar o abandono ou sequer minimização dos conceitos, modelos e ferramentas empresariais.</p>

                                <p class="Texto">Um aspecto final a destacar diz respeito aos níveis de formalismo, completude e coordenação, segundo os quais o paradigma da NGP tem sido implantado nos diferentes países. Muito frequentemente, o processo é deflagrado e tem continuidade de forma pontual, descoordenada ou por iniciativa isolada de alguns interessados, sem um compromisso formal do governo e da formulação prévia e adequada de conceitos, modelos, estratégias, programas e ações que vão dar suporte a programas de reforma administrativa.</p>

                                <p class="Texto">Embora não plenamente implantado, um modelo exemplar de programa completo e ordenado de reforma administrativa, segundo os princípios da NGP, foi o Plano de Reforma do governo federal brasileiro, no período 1995-1998, que propôs os seguintes elementos básicos: </p>

                                <p class="SubSubTitulo">Plano Diretor da Reforma do Estado</p>
                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> os objetivos e estratégias básicas da reforma, inclusive as transformações a serem empreendidas nas funções do Estado e nas entidades públicas;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> os setores fundamentais do aparelho do Estado (núcleo estratégico, atividades exclusivas, serviços não exclusivos, produção de bens e serviços para o mercado) e seus respectivos objetivos;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> as formas de propriedade no Aparelho do Estado (estatal, privada, pública não-estatal), segundo os setores do Estado;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> as formas de administração dos serviços públicos (administração pública burocrática e administração pública gerencial), segundo os setores do Estado;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> o conjunto de medidas constitucionais, complementares e ordinárias para a implantação da reforma administrativa;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> o conjunto de medidas estruturais para a definição, a criação e a implantação dos novos tipos de organização pública requeridas pela reforma (agências executivas, organizações sociais e agências reguladoras).</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">Novas formulações, leis e medidas foram sendo definidas à medida que o Programa ia sendo implantado.</p>

                                <p class="Texto">Essa foi, basicamente, a linha adotada pelo Plano Diretor da Reforma do Estado e que, embora não implantado na sua plenitude, produziu e ainda produz efeitos positivos na administração pública brasileira. Por outro lado, os recentes movimentos denominados de “choque de gestão”, que vêm ocorrendo em algumas administrações estaduais, são fortemente inspirados nos seus princípios, conceitos e ferramentas.</p>

                                <p class="Texto">No entanto, existe um aspecto do Plano Diretor que o torna singular em relação às reformas similares desenvolvidas nos diferentes países. Trata-se da consideração explícita da gestão burocrática, sob a denominação de “administração pública burocrática”, como um modelo (e não distorção) para determinados segmentos da administração pública. O modelo burocrático, segundo o Plano Diretor, seria a forma ideal de gestão para o Núcleo Estratégico do Governo (ou, pelo menos, para parte dele), para o qual, inclusive, foram estabelecidos objetivos específicos de desenvolvimento e modernização.</p>

                                <p class="SubTitulo">4.3 - Orientação para Resultados: foco básico da Nova Gestão Pública </p>

                                <p class="Texto">Não obstante, a diversidade e a variedade das experiências mundiais em Nova Gestão Pública, a “orientação para resultados” (ou para o mercado, ou para cliente ou para o desempenho) constitui uma premissa presente em praticamente todas elas, representando, na realidade, uma das suas maiores contribuições à administração pública.</p>

                                <p class="Texto">	De fato, a orientação para resultados representou uma mudança radical nos modelos da administração pública ao vislumbrar uma nova perspectiva de abordar a dinâmica e o funcionamento das organizações, retirando o foco dos meios (procedimentos, recursos e insumos) para colocá-lo nos produtos/serviços e, principalmente, nos resultados finalísticos ou transformadores (outcomes – resultados de interesse do cidadão), conforme diagrama a seguir.</p>

