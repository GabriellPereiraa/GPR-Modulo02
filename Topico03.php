                                <p class="SubTitulo">3.2 - Deficiências e Rigidez do Modelo Burocrático</p>
                                
                                <p class="Texto">A prática do modelo burocrático não atende plenamente às suas premissas e às expectativas.</p>

                                <p class="Texto">Na realidade, as críticas e as reações às ineficiências e às dificuldades de adaptação da administração burocrática surgirem já nos anos 50. De fato, na época, Robert Merton, ao analisar o modelo burocrático constatou e caracterizou uma série de deficiências teórico-conceituais, que denominou de consequências imprevistas ou disfunções da burocracia. Indo um pouco mais longe ainda, pode-se destacar que, em 1942, Philip Selznick já constatara a existência de consequências inesperadas em relação à flexibilidade da burocracia para atender às exigências dos participantes e dos clientes externos. Outra análise crítica muito compartilhada nos anos 50 foi o chamado “ciclo vicioso burocrático”, que se expressava na priorização dos meios sobre os fins, na desconsideração de clientes e usuários e em diversas manifestações de alienação dos trabalhadores.</p>

                                <p class="Texto">Todavia, foi na sua implantação nas organizações do setor público que o modelo baseado na burocracia weberiana revelou uma série de deficiências e incongruências, que levaram às administrações públicas a desempenhos e funcionamentos ineficazes e ineficientes. Além disso, a rigidez do comportamento das burocracias se mostrou completamente inadequada ao novo contexto que emergia, caracterizado por grandes incertezas e transformações radicais e bruscas. Dessa forma, o modelo burocrático não possuía a flexibilidade e a agilidade requeridas por um novo mundo dinâmico, neoliberal e globalizado que emergia.</p>

                                <p class="Texto">São muito longas e diversas as deficiências, disfunções e/ou consequências inesperadas apresentadas pelo paradigma da Administração Burocrática, segundo diferentes autores.</p>



