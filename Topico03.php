                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> transparência e critérios racionais e legais na tomada das decisões, na gestão dos negócios e no funcionamento da administração pública;</p>
