                                <h2 class="Titulo" >Tópico 3 - O paradigma da Administração Burocrática</h2>
                                <br>

                                <p class="Texto">A Administração Burocrática é o paradigma que surge, em diversas épocas e países, em oposição, e como alternativa, ao paradigma da Administração Patrimonial. De fato, as distorções e os excessos praticados, ao longo do tempo, pelo Estado e pela Administração Patrimonial revelaram uma atuação prejudicial aos interesses gerais da sociedade e incompatível com os valores e as necessidades do capitalismo industrial e da democracia parlamentar nascente.</p>

                                <p class="Texto">É justamente nesse contexto que emerge e se consolida a Administração Burocrática, inspirada no paradigma da burocracia weberiana, como expressão da racionalidade legal e gerencial. Este que passou a ser considerado como o modelo universal de gestão para o setor público, perfeitamente capaz de atender e satisfazer aos interesses, aos valores e às necessidades requeridos pela nova sociedade.</p>

                                <p class="SubTitulo">3.1 - Características Básicas da Administração</p>
                                <p class="SubSubTitulo">Burocrática</p>

                                <p class="Texto">O modelo burocrático serve aos interesses públicos, perfeitamente separáveis do interesse privado, e seus serviços são prestados com base na plena subordinação às normas e às regras preestabelecidas. O comportamento burocrático, então, torna-se previsível, com os funcionários selecionados e promovidos por mérito e protegidos de pressões políticas e sociais, voltados apenas para o atendimento aos interesses gerais.</p>
                                
                                <p class="Texto">A Administração Burocrática apresentava as seguintes características básicas:</p>

