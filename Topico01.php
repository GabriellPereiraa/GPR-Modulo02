                                <h2 class="Titulo" >Tópico 1 - Administração Pública e Paradigmas </h2>
                                <br>

                                <p class="Texto">A administração pública é um termo amplo. De acordo com Oliveira (2014, p.4) é um “processo estruturado de planejamento, organização, orientação, execução, avaliação e aprimoramento das atividades da Instituição Pública no direcionamento em atender as necessidades e expectativas dos cidadãos da comunidade, visando o bem comum”.</p>

                                <p class="Texto">Ela é essencial, necessária e a partir da Constituição de 1988 a estrutura política administrativa do Brasil (no que concerne a União, os estados, o distrito federal e os municípios) passou também a ser autônoma. </p>

                                <p class="Texto">A administração pública teve origem nas ideias iniciais de organização de Estado de Platão (429 a.C - 347 a.C) e Aristóteles (384 a.C - 322 a.C) e sofre mudanças e influências diversas no decorrer dos anos, como influências do regime político vigente, da cultura, da evolução histórica, da ética e a moral dos governantes e da globalização (OLIVEIRA, 2014). A evolução dos modelos em administração pública serão apresentados adiante.</p>

                                <p class="Texto">De acordo com o dicionário Aurélio da língua portuguesa, a palavra paradigma deriva do grego "parádeigma, atos", com sentido de padrão, modelo. Os paradigmas da administração pública têm evoluído, ao longo dos tempos, partindo da Administração Patrimonial, no início do século xx, passando pela Administração Burocrática, até a Nova Gestão Pública (NGP), que reinou quase absoluta nas décadas de 1980 e de 1990. No entanto, a partir do final do século passado, surge um novo paradigma, genericamente denominado de Governança Pública, com foco na colaboração e na coordenação dos entes públicos e destes com a sociedade civil.</p>

                                <p class="Texto">Ao longo da evolução dos referidos paradigmas, vários conceitos, modelos, sistemas e ferramentas de administração pública foram criados, transformados, consolidados e, até mesmo, abandonados. Cada etapa sofreu impactos das anteriores, influenciou e aprimorou as seguintes, proporcionando importantes lições e abrindo novas oportunidades no processo permanente de modernização, reforma e gestão da administração pública.</p>

                                <div class="ImportanteCaixa">
                                    <p class="ImportanteTitulo"> Importante</p>
                                    <p class="ImportanteTexto">É importante destacar, todavia, que não se trata de uma evolução estritamente linear e segmentada. De fato, o surgimento ou o predomínio de um novo paradigma na Administração Pública, em qualquer momento, não significa a anulação ou abandono dos anteriores, mas sim a busca de um novo equilíbrio, visando a uma correção de um rumo ou à introdução de uma forma mais efetiva para enfrentar problemas e desafios emergentes. Muitas vezes, os paradigmas encontram-se sobrepostos e caminham juntos, com avanços e retrocessos em um movimento pendular.</p>
                                </div>
