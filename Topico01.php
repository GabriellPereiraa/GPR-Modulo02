                                    Em decorrência, três aspectos-chave devem ser considerados nos processos de reforma e modernização de uma administração pública:
                                </p>

                                <ul style="list-style-type: lower-alpha;">
                                    <li>
                                        <p class="Texto">Em qualquer momento, uma administração pública específica estará submetida, com maior ou menor intensidade e amplitude, a um conjunto de influências, positivas ou negativas, dos diferentes paradigmas de administração pública;</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Na construção de um novo modelo para a modernização e gestão de uma administração pública, deverão ser consideradas as contribuições relevantes dos diferentes paradigmas, considerando as diretrizes emanadas dos poderes constituídos, as pressões e tendências do contexto e a realidade sobre a qual o modelo vai ser aplicado;</p>
                                    </li>
                                    <li>
